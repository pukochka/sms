export function mapServiceTitle<
  T extends SMSServices | SMSRentService | SMSMultiService
>(value: Array<T>): Array<T> {
  return value
    .map((item) => {
      item.longName = products[item.name]?.toString() ?? item.name;
      return item;
    })
    .filter((item) => !(item.name.length > 2));
}

